function sidebar_save_post_meta(){  
    global $post;    
  		
    if ( defined('DOING_AUTOSAVE') && DOING_AUTOSAVE ){  
        return $post_id;  
    }else{  
		$posts = getAllPages();
		$csv="";
		$count=0;
		foreach($posts as $page){
			$title = get_the_title($page->ID);
			$ID = $page->ID;
			if(isset($_POST["checkbox$ID"])){
			if($count>0)
				$csv.=",";
			$csv.= "$ID";
			$count++;
			}
		}
        update_post_meta($post->ID, "sidebar-post-meta-displayedon", $csv);  
    }  
}
